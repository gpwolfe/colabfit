UNITS = create_units("2014")
BOHR_TO_ANG = UNITS["Bohr"]

DATA_PATH = Path(
    "data/datasets--microsoft--msr-acc-tae25/blobs/4b30596e45e4b4c9842c53d2a7cbc50210bf21a8cb455bdfde0c953c99148898"
)


def reader(filepath):
    pf = pq.ParquetFile(filepath)
    for batch in pf.iter_batches():
        table = batch.to_pydict()
        for i in range(len(table["sha1"])):
            symbols = table["symbols"][i]
            positions = [[x * BOHR_TO_ANG for x in coord] for coord in table["geometry"][i]]
            atoms = AseAtoms(symbols=symbols, positions=positions, pbc=False)
            atoms.info = {
                "_name": f"{DATASET_NAME}__{table['sha1'][i]}",
                "tae": table["tae@w1-f12"][i],
                "sha1": table["sha1"][i],
                "molecular_charge": int(table["molecular_charge"][i]),
                "molecular_multiplicity": table["molecular_multiplicity"][i],
            }
            yield AtomicConfiguration.from_ase(atoms)


property_map = PropertyMap([atomization_energy_pd])
property_map.set_metadata_field("software", "Molpro 2024.1")
property_map.set_metadata_field("method", "W1-F12/CCSD(T)-CBS")
property_map.set_metadata_field(
    "input",
    {
        "basis_HF_CCSD": ("cc-pVDZ-F12 and cc-pVTZ-F12, CBS extrapolation alpha=5"),
        "basis_delta_T": ("jul-cc-pV(D+d)Z and jul-cc-pV(T+d)Z, extrapolation alpha=3.22"),
        "basis_core_valence": "cc-pwCVTZ",
        "geometry_optimization": "B3LYP-D3(BJ)/def2-TZVPP (ORCA)",
        "conformational_sampling": "GFN2-xTB then r2SCAN-3c",
        "multireference_filter": "%TAE[(T)] <= 6% at CCSD(T)/6-31G*",
        "spin_state_filter": "S0-T1 gap >= 0 at B3LYP/def2-TZVP",
    },
)
property_map.set_metadata_field("sha1", "sha1", dynamic=True)

tae_info = PropertyInfo(
    property_name="atomization-energy",
    field="tae",
    units="hartree",
    original_file_key="tae@w1-f12",
    additional=[("per-atom", {"value": False, "units": None})],
)
property_map.set_properties([tae_info])
PROPERTY_MAP = property_map.get_property_map()

gen = reader(DATA_PATH)

dm = DataManager(
    prop_defs=[atomization_energy_pd],
    configs=gen,
    prop_map=PROPERTY_MAP,
    dataset_id=DATASET_ID,
    standardize_energy=True,
    read_write_batch_size=10000,
)

dm.load_co_po_to_vastdb(loader, batching_ingest=True, check_existing=False)

dm.create_dataset(
    loader=loader,
    name=DATASET_NAME,
    authors=AUTHORS,
    publication_link=PUBLICATION,
    data_link=DATA_LINK,
    other_links=OTHER_LINKS,
    description=DESCRIPTION,
    data_license=LICENSE,
    publication_year=PUBLICATION_YEAR,
    equilibrium=True,
    date_requested="2026-05-11",
)
