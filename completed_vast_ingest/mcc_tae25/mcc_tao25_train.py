"""
MSR-ACC-TAE25_Train ingest script for ColabFit VAST database.

Properties
----------
atomization-energy: total atomization energy at CCSD(T)/CBS (W1-F12) in eV

File notes
----------
Source: datasets--microsoft--msr-acc-tae25/blobs/
        4b30596e45e4b4c9842c53d2a7cbc50210bf21a8cb455bdfde0c953c99148898
        (71,871 rows; training split)

MSR-ACC/TAE25 provides 73,040 total atomization energies at the CCSD(T)/CBS
level using the W1-F12 thermochemical protocol (Molpro 2024.1) for closed-shell,
charge-neutral, covalently bound equilibrium molecules with up to 5 non-hydrogen
atoms from elements H–Ar (excluding rare gases). Geometries in the parquet file
are in bohr and are converted to Angstrom in the reader. TAE values are in
hartree in the source; colabfit-tools converts to eV during ingest.
"""

